<script>
	import { Button, Input, Label, FormGroup, RichTextEditor } from '$lib';
	import { UploadDropzone, createUploader } from '$lib/uploadthing';
	import '@uploadthing/svelte/styles.css';

	let { isOpen = false, event = null, onSave, onCancel } = $props();

	const uploader = createUploader('imageUploader', {
		onClientUploadComplete: (res) => {
			console.log('Files: ', res);
			if (res && res.length > 0) {
				// We return { uploadedBy, url } from core.js
				const fileUrl = res[0].serverData?.url || res[0].url;
				formData.image = fileUrl;
				imagePreview = fileUrl;
			}
		},
		onUploadError: (error) => {
			console.error(error);
			uploadError = error.message;
		}
	});

	// Form state
	let formData = $state({
		title: '',
		description: '',
		isMultiDay: false,
		days: [{ date: new Date().toISOString().split('T')[0], startTime: '', endTime: '' }],
		date: new Date().toISOString().split('T')[0],
		location: '',
		address: '',
		mapsLink: '',
		image: '',
		link: ''
	});

	// UI state
	let imagePreview = $state(null);
	let isUploading = $state(false);
	let uploadError = $state(null);
	let isAutoGeneratedMapsLink = $state(true); // Track if maps link is auto-generated

	// Load event data when editing
	$effect(() => {
		if (event) {
			let days = [];
			let isMultiDay = false;

			if (event.days && event.days.length > 0) {
				days = JSON.parse(JSON.stringify(event.days));
				isMultiDay = event.isMultiDay || false;
			} else {
				days = [
					{
						date: event.date
							? new Date(event.date).toISOString().split('T')[0]
							: new Date().toISOString().split('T')[0],
						startTime: event.startTime || '',
						endTime: event.endTime || ''
					}
				];
				isMultiDay = false;
			}

			formData = {
				title: event.title || '',
				description: event.description || '',
				isMultiDay,
				days,
				date: event.date
					? new Date(event.date).toISOString().split('T')[0]
					: new Date().toISOString().split('T')[0],
				location: event.location || '',
				address: event.address || '',
				mapsLink: event.mapsLink || '',
				image: event.image || '',
				link: event.link || ''
			};
			// Set preview for existing image
			if (event.image) {
				imagePreview = event.image;
			} else {
				imagePreview = null;
			}
		} else {
			formData = {
				title: '',
				description: '',
				isMultiDay: false,
				days: [{ date: new Date().toISOString().split('T')[0], startTime: '', endTime: '' }],
				date: new Date().toISOString().split('T')[0],
				location: '',
				address: '',
				mapsLink: '',
				image: '',
				link: ''
			};
			imagePreview = null;
		}
		uploadError = null;

		// Check if the loaded event has a custom maps link
		if (event?.mapsLink && event.mapsLink.trim()) {
			isAutoGeneratedMapsLink = false;
		} else {
			isAutoGeneratedMapsLink = true;
		}
	});

	// Auto-generate Google Maps link from address
	$effect(() => {
		const address = formData.address;

		// Only auto-generate if address exists and link is auto-generated (not manually edited)
		if (address && address.trim() && isAutoGeneratedMapsLink) {
			// Generate Google Maps search URL
			const encodedAddress = encodeURIComponent(address.trim());
			const generatedLink = `https://www.google.com/maps/search/?api=1&query=${encodedAddress}`;
			formData.mapsLink = generatedLink;
		} else if (!address || !address.trim()) {
			// Clear the link if address is cleared
			if (isAutoGeneratedMapsLink) {
				formData.mapsLink = '';
			}
		}
	});

	// Clear image
	function clearImage() {
		formData.image = '';
		imagePreview = null;
	}

	async function handleSubmit(e) {
		e.preventDefault();

		const eventData = {
			id: event?.id || crypto.randomUUID(),
			title: formData.title,
			description: formData.description,
			isMultiDay: formData.isMultiDay,
			days: formData.days,
			date: formData.days[0]?.date || formData.date,
			location: formData.location,
			address: formData.address,
			mapsLink: formData.mapsLink,
			image: formData.image,
			link: formData.link
		};

		onSave(eventData);
	}

	function handleCancel() {
		onCancel();
	}
</script>

{#if isOpen}
	<div
		class="modal-overlay"
		onclick={handleCancel}
		onkeydown={(e) => e.key === 'Escape' && handleCancel()}
		role="button"
		tabindex="-1"
	>
		<div
			class="modal-container"
			onclick={(e) => e.stopPropagation()}
			onkeydown={(e) => e.stopPropagation()}
			role="dialog"
