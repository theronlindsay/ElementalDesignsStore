		>
			<div class="modal-header">
				<h2 class="modal-title">
					{event ? 'Edit Event' : 'Add New Event'}
				</h2>
				<button class="close-btn" onclick={handleCancel} aria-label="Close modal">
					<i class="fas fa-times"></i>
				</button>
			</div>

			<form onsubmit={handleSubmit} class="modal-form">
				<FormGroup>
					<Label htmlFor="title" required>Event Title</Label>
					<Input
						type="text"
						name="title"
						bind:value={formData.title}
						placeholder="e.g., Renaissance Fair 2025"
						required
					/>
				</FormGroup>

				<FormGroup>
					<Label htmlFor="description" required>Description</Label>
					<RichTextEditor
						value={formData.description}
						onchange={(html) => { formData.description = html; }}
						placeholder="Describe your event..."
					/>
				</FormGroup>

				<div class="form-row">
					<FormGroup>
						<Label htmlFor="isMultiDay">Event Type</Label>
						<div class="toggle-container">
							<label class="toggle-label">
								<input type="checkbox" bind:checked={formData.isMultiDay} />
								<span class="toggle-slider"></span>
								<span class="toggle-text"
									>{formData.isMultiDay ? 'Multi-Day Event' : 'Single Day Event'}</span
								>
							</label>
						</div>
					</FormGroup>

					<FormGroup>
						<Label htmlFor="location">Location Name</Label>
						<Input
							type="text"
							name="location"
							bind:value={formData.location}
							placeholder="e.g., Downtown Plaza"
						/>
					</FormGroup>
				</div>

				<div class="days-container">
					<Label required>Event Date(s) & Times</Label>
					{#each formData.days as day, index}
						<div class="day-row-edit">
							<FormGroup>
								<Label htmlFor={`date-${index}`} required={index === 0}>Date</Label>
								<Input type="date" id={`date-${index}`} bind:value={day.date} required />
							</FormGroup>
							<FormGroup>
								<Label htmlFor={`start-${index}`}>Start Time</Label>
								<Input type="time" id={`start-${index}`} bind:value={day.startTime} />
							</FormGroup>
							<FormGroup>
								<Label htmlFor={`end-${index}`}>End Time</Label>
								<Input type="time" id={`end-${index}`} bind:value={day.endTime} />
							</FormGroup>
							{#if formData.isMultiDay && formData.days.length > 1}
								<button
									type="button"
									class="remove-day-btn"
									onclick={() => (formData.days = formData.days.filter((_, i) => i !== index))}
									aria-label="Remove day"
								>
									<i class="fas fa-trash"></i>
								</button>
							{/if}
						</div>
					{/each}
					{#if formData.isMultiDay}
						<button
							type="button"
							class="add-day-btn"
							onclick={() =>
								(formData.days = [
									...formData.days,
									{ date: new Date().toISOString().split('T')[0], startTime: '', endTime: '' }
								])}
						>
							<i class="fas fa-plus"></i> Add Another Day
						</button>
					{/if}
				</div>

				<!-- Address and Maps Link -->
				<FormGroup>
					<Label htmlFor="address">Street Address</Label>
					<Input
						type="text"
						name="address"
						bind:value={formData.address}
						placeholder="e.g., 123 Main St, City, State 12345"
					/>
				</FormGroup>

				<FormGroup>
					<Label htmlFor="mapsLink">Google Maps Link</Label>
					<Input
						type="text"
						name="mapsLink"
						bind:value={formData.mapsLink}
						placeholder="Auto-generated from address or paste custom link..."
						oninput={() => {
							isAutoGeneratedMapsLink = false;
						}}
					/>
					<p class="field-hint">
						<i class="fas fa-magic"></i>
						Auto-filled when you enter an address above
					</p>
				</FormGroup>

				<FormGroup>
					<Label htmlFor="image">Event Image</Label>
					<div class="image-upload-section">
						{#if imagePreview}
							<div class="image-preview">
								<img src={imagePreview} alt="Event preview" />
								<button
									type="button"
									class="clear-image-btn"
									onclick={clearImage}
									disabled={isUploading}
								>
									<i class="fas fa-trash"></i>
									Remove Image
								</button>
							</div>
						{:else}
							<div class="upload-area">
								<UploadDropzone {uploader} />
							</div>
						{/if}

						{#if uploadError}
							<p class="error-message">{uploadError}</p>
						{/if}

						{#if isUploading}
							<p class="uploading-message">Uploading image...</p>
						{/if}
					</div>
				</FormGroup>

				<FormGroup>
					<Label htmlFor="link">Event Link</Label>
					<Input
						type="text"
						name="link"
						bind:value={formData.link}
						placeholder="https://example.com/event-details"
					/>
				</FormGroup>

				<div class="modal-actions">
					<Button type="button" variant="ghost" onclick={handleCancel}>Cancel</Button>
					<Button type="submit" variant="primary">
						<i class="fas fa-save"></i>
						<span>{event ? 'Update' : 'Create'} Event</span>
					</Button>
				</div>
			</form>
		</div>
	</div>
{/if}

